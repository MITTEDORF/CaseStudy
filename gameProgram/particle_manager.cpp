//=============================================================================
// 初期化
//=============================================================================
void CParticleManager::Init(LPDIRECT3DDEVICE9 device)
{
	for(int i=0;i<PARTICLE_MAX-1;i++)
	{
		object[i]=CParticleObject::Create(device);
	}
}
//=============================================================================
// パーティクルの生成開始
//=============================================================================
void CParticleManager::Start()
{
}
//=============================================================================
// 全パーティクルの停止
//=============================================================================
void CParticleManager::StopALL()
{
	for(int i=0;i<PARTICLE_MAX-1;i++)
	{
		object[i]->Destroy();
	}
}

