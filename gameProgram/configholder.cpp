int CConfigHolder::Config[CONFIG_MAX];
//=============================================================================
// セット
//=============================================================================
void CConfigHolder::Set(CONFIG_ID id,int value)
{
	if(id<0||id>=CONFIG_MAX){return;}

	Config[id]=value;
}
//=============================================================================
// ゲット
//=============================================================================
int CConfigHolder::Get(CONFIG_ID id)
{
	if(id<0||id>=CONFIG_MAX){return (CONFIG_MAX);}

	return (Config[id]);
}